    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String doctorName = txtDoctorName.getText().trim();
        String doctorSpecialization = txtDoctorSpeaciality.getText().trim();

        if (doctorName.isEmpty() || doctorSpecialization.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Construct the doctor document to insert
        Document newDoctor = new Document();
        newDoctor.append("doctorName", doctorName);
        newDoctor.append("doctorSpecialization", doctorSpecialization);
        newDoctor.append("isInstructor", false);
        newDoctor.append("isResidentDoctor", false);
        newDoctor.append("organizationType", "Doctor");
        newDoctor.append("hospitalId", hospital.getHospitalId().toString());
        newDoctor.append("userId", hospital.getHospitalAdminId());

        // Insert the new doctor into the MongoDB collection
        try {
            doctorCollection.insertOne(newDoctor);
            JOptionPane.showMessageDialog(this, "Doctor added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            txtDoctorName.setText("");  // Clear the text fields after successful insertion
            txtDoctorSpeaciality.setText("");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding doctor: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

