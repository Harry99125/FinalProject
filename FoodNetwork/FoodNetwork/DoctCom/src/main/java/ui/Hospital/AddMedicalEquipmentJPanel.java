    private void btnSubmitRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitRequestActionPerformed
        // TODO add your handling code here:
        String SelectedSupplier = cmbSuppliers.getSelectedItem().toString();
        String SelectedEquipment = cmbEquipments.getSelectedItem().toString();
        Document supplier = crudOperations.getFirstRecordByKey("supplierName", SelectedSupplier, supplierCollection);
        Document equipment = crudOperations.getFirstRecordByKey("equipmentName", SelectedEquipment, equipmentCollection);

        Document request = new Document();
        request.append("hospitalId", hospital.getHospitalId().toString());
        request.append("supplierId", supplier.get("_id").toString());
        request.append("equipmentId", equipment.get("_id").toString());
        request.append("status", "Pending");

        crudOperations.insertDocument(request, requestEquipmentCollection);


    }//GEN-LAST:event_btnSubmitRequestActionPerformed

