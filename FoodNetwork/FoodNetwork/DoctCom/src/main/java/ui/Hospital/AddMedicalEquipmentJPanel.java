    private void populatePurpose() {
//        show selected equipments pupose
        String SelectedEquipment = cmbEquipments.getSelectedItem().toString();
        Document equipment = crudOperations.getFirstRecordByKey("equipmentName", SelectedEquipment, equipmentCollection);
        txtPurpose.setText(equipment.getString("equipmentPurpose"));
    }

    private void populateEquipmentComboBox() {
        String SelectedSupplier = cmbSuppliers.getSelectedItem().toString();
        Document supplier = crudOperations.getFirstRecordByKey("supplierName", SelectedSupplier, supplierCollection);
        System.out.println(supplier.get("_id") + "cfgvhbj");
        FindIterable<Document> supplierEquipmentList = crudOperations.getRecordsByKey("supplierId", String.valueOf(supplier.get("_id")), supplierEquipmentCollection);
        for (Document supplierEquipment : supplierEquipmentList) {
            System.out.println(supplierEquipment.get("equipmentId") + "dfcvv");
            Document equipment = crudOperations.getFirstRecordByKey("_id", new ObjectId(supplierEquipment.get("equipmentId").toString()), equipmentCollection);
            cmbEquipments.addItem(equipment.getString("equipmentName"));
        }
    }

    private void populateSupplierComboBox() {
        FindIterable<Document> supplierList = crudOperations.getRecordsByKey("organizationType", "Supplier", supplierCollection);
        for (Document supplier : supplierList) {
            cmbSuppliers.addItem(supplier.getString("supplierName"));
        }
    }

