    private void btnAddRetailerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRetailerActionPerformed
        // TODO add your handling code here:
    // Retrieve inputs from GUI components
    String retailerName = txtRetailerName.getText().trim();
    String retailerLocation = txtRetailerLocation.getText().trim();
    String retailerContact = txtRetailerContact.getText().trim();
    String retailerManagerName = txtRetailerManagerName.getText().trim();
    String retailerManagerPass = txtRetailerManagerPass.getText().trim();
    String retailerEmail = txtRetailerEmail.getText().trim();

    // Validate inputs
    if (retailerName.isEmpty() || retailerLocation.isEmpty() || retailerContact.isEmpty()
            || retailerManagerName.isEmpty() || retailerManagerPass.isEmpty() || retailerEmail.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "All fields are mandatory.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!retailerEmail.contains("@") || !retailerEmail.contains(".")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Invalid email format.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Check if the retailer already exists in retailerCollection
        Document existingRetailer = retailerCollection.find(new Document("email", retailerEmail)).first();
        if (existingRetailer != null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Retailer with this email already exists.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Check if the user already exists in userCollection
        Document existingUser = userCollection.find(new Document("email", retailerEmail)).first();
        if (existingUser != null) {
            javax.swing.JOptionPane.showMessageDialog(this, "User with this email already exists.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Add the manager as a user in userCollection
        Document user = new Document("email", retailerEmail)
                .append("username", retailerManagerName)
                .append("password", retailerManagerPass)
                .append("role", "Retailer");
        userCollection.insertOne(user);
        Document userDoc = userCollection.find(new Document("username", retailerName)).first();

        // Add the retailer to retailerCollection
        Document retailer = new Document("RetailerName", retailerName)
                .append("location", retailerLocation)
                .append("contact", retailerContact)
                .append("managerName", retailerManagerName)
                .append("managerPassword", retailerManagerPass)
                .append("email", retailerEmail)
                .append("organizationType", "Retailer")
                .append("RetailerAdminId",  String.valueOf(userDoc.getObjectId("_id")));
        
        retailerCollection.insertOne(retailer);

        

        // Success message
        javax.swing.JOptionPane.showMessageDialog(this, "Retailer and User added successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        // Clear the input fields
        txtRetailerName.setText("");
        txtRetailerLocation.setText("");
        txtRetailerContact.setText("");
        txtRetailerManagerName.setText("");
        txtRetailerManagerPass.setText("");
        txtRetailerEmail.setText("");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error while adding retailer: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }





            
    }//GEN-LAST:event_btnAddRetailerActionPerformed


